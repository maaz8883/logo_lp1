        <script>
            document.addEventListener('DOMContentLoaded', function () {
                const slides = document.querySelectorAll('.testi_slide');
                const totalSlides = slides.length;
                let current = 0;

                const positions = ['pos1', 'pos2', 'pos3', 'pos4', 'pos5', 'pos6', 'pos7'];
                const visibleSlides = positions.length;

                function updateSlides() {
                    // remove all position classes
                    slides.forEach(slide => {
                        slide.classList.remove(...positions, 'active');
                    });

                    // assign only 7 positions in loop
                    for (let i = 0; i < visibleSlides; i++) {
                        const index = (current + i) % totalSlides;
                        slides[index].classList.add(positions[i]);

                        // optional: active center slide
                        if (positions[i] === 'pos2') {
                            slides[index].classList.add('active');
                        }
                    }
                }

                document.querySelector('.next_arrow').addEventListener('click', () => {
                    current = (current + 1) % totalSlides;
                    updateSlides();
                });

                document.querySelector('.prev_arrow').addEventListener('click', () => {
                    current = (current - 1 + totalSlides) % totalSlides;
                    updateSlides();
                });

                updateSlides();
            });
        </script>

